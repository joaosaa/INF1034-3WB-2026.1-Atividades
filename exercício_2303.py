from turtle import *
from time import sleep

t = Turtle()
t.speed(100)

#Polônia

t.pu()
t.goto(-250,125)
t.pd()
for _ in range(2):
    t.fd(500)
    t.rt(90)
    t.fd(150)
    t.rt(90)

